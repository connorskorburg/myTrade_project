function openNav(){
    document.getElementById('nav-toggle').style.width = "15%";
    document.getElementById("all-content").style.width = "85%";
    document.getElementById("all-content").style.marginLeft = "15%";
}
function closeNav(){
    document.getElementById('nav-toggle').style.width = "0";
    document.getElementById("all-content").style.width = "100%";
    document.getElementById("all-content").style.marginLeft = "0";
}